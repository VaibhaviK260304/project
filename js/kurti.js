function inc()
{
    const quantitytext  = document.getElementById("qty");
    const showquantity = document.getElementById("quantity-text");

    const  totaltext = document.getElementById("total-text") ;

    let quantity = parseInt(quantitytext.innerText);

    if(quantity<10)
    {
        quantitytext.innerText = ++quantity;
    }
    else{
        alert("Maximum Quantity Reached!");
    }
    showquantity.innerText=`Quantity : ${quantity} x 599`;
    totaltext.innerText= `Total :  ${quantity*599}`;
}
function dec()
{
    const quantitytext  = document.getElementById("qty");
    const showquantity = document.getElementById("quantity-text");
    const  totaltext = document.getElementById("total-text") ;

    let quantity = parseInt(quantitytext.innerText);
    
    if(quantity > 1)
    {
    quantitytext.innerText = --quantity;
    }
    else
    {
        alert('Quantity cannot be less than 1');
    }
    showquantity.innerText=`Quantity : ${quantity} x 599`;
    totaltext.innerText= `Total :  ${quantity*599}`;
}
